import math #importa a biblioteca Math (operações matemáticas)

print(math.sqrt(36)) #Calcula a rais quadrada de um número

print(math.factorial(6)) #Calcula o fatorial de um número

print(math.gcd(40, 50, 60)) #Calcula o máximo divisor comum entre três números

print(math.lcm(40, 50, 60)) #Calcula o mínimo múltiplo comum entre três números

